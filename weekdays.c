#include<stdio.h>
void main()
{
    int a;
    scanf("%d",&a);
    if(a==1)
        printf("1-Monday");
    else if(a==2)
        printf("2-Tuesday");
    else if(a==3)
        printf("3-Wednesday");
    else if(a==4)
        printf("4-Thursday");
    else if(a==5)
        printf("5-Friday");
    else if(a==6)
        printf("6-Saturday");
    else
        printf("7-Sunday");


}
